/**
 * Applique les traductions √† la page selon la langue s√©lectionn√©e
 */
function applyTranslations() {
    const currentLang = getCurrentLanguage();
    const langTranslations = translations[currentLang] || translations.fr;

    // Traduire tous les √©l√©ments avec data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (langTranslations[key]) {
            element.textContent = langTranslations[key];
        }
    });

    // Traduire les placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (langTranslations[key]) {
            element.placeholder = langTranslations[key];
        }
    });

    console.log('‚úÖ Traductions appliqu√©es:', currentLang);
}

// Appliquer les traductions au chargement
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyTranslations);
} else {
    applyTranslations();
}

// Rendre les fonctions accessibles globalement
window.applyTranslations = applyTranslations;
window.translations = translations;
