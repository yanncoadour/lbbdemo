/**
 * Fonction pour obtenir une traduction
 * @param {string} key - Cl√© de traduction (ex: 'nav.home')
 * @param {string} lang - Code langue (ex: 'en')
 * @returns {string} - Texte traduit
 */
function getTranslation(key, lang = 'fr') {
    const keys = key.split('.');
    let value = translations;

    for (const k of keys) {
        value = value[k];
        if (!value) return key; // Retourne la cl√© si traduction non trouv√©e
    }

    return value[lang] || value['fr'] || key;
}

/**
 * Fonction pour traduire toute la page
 * @param {string} lang - Code langue
 */
function translatePage(lang) {
    console.log('üåç Traduction de la page en:', lang);

    let translatedCount = 0;
    let errorCount = 0;

    // Traduire tous les √©l√©ments avec l'attribut data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key, lang);

        // Debug: v√©rifier si la traduction a √©t√© trouv√©e
        if (translation === key) {
            console.warn(`‚ö†Ô∏è Traduction non trouv√©e pour: ${key}`);
            errorCount++;
        } else {
            translatedCount++;
        }

        // G√©rer les diff√©rents types d'√©l√©ments
        if (element.tagName === 'INPUT') {
            if (element.type === 'text' || element.type === 'email') {
                element.placeholder = translation;
            }
        } else if (element.children.length === 0) {
            // Si l'√©l√©ment n'a pas d'enfants, on peut directement remplacer le texte
            element.textContent = translation;
        } else {
            // Si l'√©l√©ment a des enfants, on remplace seulement les n≈ìuds de texte
            // En parcourant les childNodes et en rempla√ßant les Text nodes
            let textReplaced = false;
            element.childNodes.forEach(node => {
                if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
                    node.textContent = translation;
                    textReplaced = true;
                }
            });
            // Si aucun n≈ìud texte n'a √©t√© trouv√©, remplacer tout le textContent
            if (!textReplaced) {
                element.textContent = translation;
            }
        }
    });

    console.log(`‚úÖ Traduction termin√©e: ${translatedCount} √©l√©ments traduits, ${errorCount} erreurs`);
}

